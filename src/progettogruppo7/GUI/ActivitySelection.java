    private void selectionWeekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectionWeekActionPerformed
        String answer=JOptionPane.showInputDialog("Select the week");
        
        LinkedList<Integer> weeks=activities.getWeeks();
        if(weeks==null)
            JOptionPane.showMessageDialog(this, "There aren't any activities registered", "Error",JOptionPane.ERROR_MESSAGE );
        else if(weeks.contains(Integer.valueOf(answer))){
            DefaultTableModel tabmod=(DefaultTableModel)activitySelectionTable.getModel();
            tabmod.setRowCount(0);
            for(Activity a: activities.getActivities().values()){
                if(a.getWeek()==Integer.valueOf(answer)){
                    Object []o={a.getActivityID(), a.getSite(), null, a.getEstimatedTime()};
                    tabmod=(DefaultTableModel)activitySelectionTable.getModel();
                    tabmod.addRow(o);
                }

            }
            selectionWeek.setText(answer);
        }
        else
           JOptionPane.showMessageDialog(this, "  The week selected doesn't exist  ", "Error",JOptionPane.ERROR_MESSAGE );
         
            
    }//GEN-LAST:event_selectionWeekActionPerformed

