    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int answer = JOptionPane.showConfirmDialog(this, "You are removing this employee. Are you sure?");
       if (answer == JOptionPane.YES_OPTION){
            admin.removeUser(employee);
            jdbc_admin.removeUserFromDatabase(employee.getUsername(), employee.getRole());
            flag = false;
            onClose();
       }       
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String answer = JOptionPane.showInputDialog(this, "New Username:","Username");
        
        if (answer != null){
            User found = ((SystemAdmin) admin).changeUsername(employee, answer);
            while (found == null && answer != null){
                JOptionPane.showMessageDialog(this, "This username is not available");
                answer = JOptionPane.showInputDialog(this, "New Username:");
                found = ((SystemAdmin) admin).changeUsername(employee, answer);
            }
            if (answer != null)
                this.jLabel3.setText(answer);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String answer = JOptionPane.showInputDialog(this, "Old Password:" + employee.getPassword());
        
        if (answer != null){
            User found = ((SystemAdmin) admin).changePassword(employee, answer);
            while (found == null){
                JOptionPane.showMessageDialog(this, "This username is not available");
                answer = JOptionPane.showInputDialog(this, "New Username:");
                found = ((SystemAdmin) admin).changePassword(employee, answer);
            }
            
        }
    }//GEN-LAST:event_jButton4ActionPerformed

