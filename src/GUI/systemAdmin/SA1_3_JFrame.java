    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        this.week = Integer.valueOf(JOptionPane.showInputDialog(this,"Insert a week number pls", "Input"));
        int rows = tableModel.getRowCount();
        int columns = tableModel.getColumnCount();
        
        for(int i = 0; i < tableModel.getRowCount(); i++ ){
            for (int j = 1; j< tableModel.getColumnCount(); j++){
                Maintainer m = (Maintainer) employee; 
                String[] day = jTable1.getColumnName(j).split(" ");
                int value = m.getSlotAvailability(week, day[1], i);
                tableModel.setValueAt(value,i, j);
            }
        }       
        
    }//GEN-LAST:event_jButton1ActionPerformed

